import UIKit

extension UIColor {
    
    public class func catskillWhite() -> UIColor {
        return UIColor(hex: 0xEDF3F7)
    }
    
    public class func blueBayoux() -> UIColor {
        return UIColor(hex: 0x4D6478)
    }
    
    public class func pickledBluewood() -> UIColor {
        return UIColor(hex: 0x2A3C4B)
    }
    
    public class func softGray() -> UIColor {
        return UIColor(hex: 0xA6B0B6)
    }
}

